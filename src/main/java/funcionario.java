
public class funcionario {
    private int id_fucionario;
    private String nome_fucionario;
    private String cargo;
    private String horario_trabalho;

    /**
     * @return the id_fucionario
     */
    public int getId_fucionario() {
        return id_fucionario;
    }

    /**
     * @param id_fucionario the id_fucionario to set
     */
    public void setId_fucionario(int id_fucionario) {
        this.id_fucionario = id_fucionario;
    }

    /**
     * @return the nome_fucionario
     */
    public String getNome_fucionario() {
        return nome_fucionario;
    }

    /**
     * @param nome_fucionario the nome_fucionario to set
     */
    public void setNome_fucionario(String nome_fucionario) {
        this.nome_fucionario = nome_fucionario;
    }

    /**
     * @return the cargo
     */
    public String getCargo() {
        return cargo;
    }

    /**
     * @param cargo the cargo to set
     */
    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    /**
     * @return the horario_trabalho
     */
    public String getHorario_trabalho() {
        return horario_trabalho;
    }

    /**
     * @param horario_trabalho the horario_trabalho to set
     */
    public void setHorario_trabalho(String horario_trabalho) {
        this.horario_trabalho = horario_trabalho;
    }
 
}