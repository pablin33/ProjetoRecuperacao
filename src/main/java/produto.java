
public class produto {
    private int id_produto;
    private String nome_produto;
    private int preco;
    private String codigo_de_barra;

    /**
     * @return the id_produto
     */
    public int getId_produto() {
        return id_produto;
    }

    /**
     * @param id_produto the id_produto to set
     */
    public void setId_produto(int id_produto) {
        this.id_produto = id_produto;
    }

    /**
     * @return the nome_produto
     */
    public String getNome_produto() {
        return nome_produto;
    }

    /**
     * @param nome_produto the nome_produto to set
     */
    public void setNome_produto(String nome_produto) {
        this.nome_produto = nome_produto;
    }

    /**
     * @return the preco
     */
    public int getPreco() {
        return preco;
    }

    /**
     * @param preco the preco to set
     */
    public void setPreco(int preco) {
        this.preco = preco;
    }

    /**
     * @return the codigo_de_barra
     */
    public String getCodigo_de_barra() {
        return codigo_de_barra;
    }

    /**
     * @param codigo_de_barra the codigo_de_barra to set
     */
    public void setCodigo_de_barra(String codigo_de_barra) {
        this.codigo_de_barra = codigo_de_barra;
    }

   
}